    static String MyName = null;
    public Activitate(String MyName) {
        initComponents();
        setIcon();
        this.MyName = MyName;
        I_BD bd = null;
        String url = "rmi://" + adresa + ":" + 1099 + "/bd";
        try {
            bd = (I_BD)Naming.lookup(url);
            if (bd.afla_categorie(MyName).equals("Elev")) {
                UseNrDiscutii.setText(bd.nr_discutii(bd.afla_id_elev(MyName)) + "");
                int valoare = bd.durata_totala_elevi(bd.afla_id_elev(MyName));
                if (valoare > 1)
                    UseDurataTotala.setText(valoare + " minute");
                else UseDurataTotala.setText(1 + " minute");
                ArrayList<String> istoric = bd.istoric(bd.afla_id_elev(MyName));
                UseRating.setVisible(true);
                Istoric.setText("Istoric");
                for (  int i = 0; i < istoric.size(); i = i+4 )
                {
                    UseRating.setText( UseRating.getText() + "\n" + "Materie: " + istoric.get(i) + " - Nume: " + istoric.get(i+1) + " - Numar Conversatii: " + istoric.get(i+2) + " - Durata: " + istoric.get(i+3) + " minute \n" );
                }
            } else {
                UseNrDiscutii.setText(bd.nr_discutii_prof(bd.afla_id_prof(MyName)) + "");
                int valoare = bd.durata_totala_prof(bd.afla_id_prof(MyName));
                if (valoare > 1)
                    UseDurataTotala.setText(valoare + " minute");
                else UseDurataTotala.setText(1 + " minute");
                Istoric.setText("Rating");
                UseRating.setVisible(true);
                UseRating.setText(bd.rating(bd.afla_id_prof(MyName)) + " %");
                
            }
        } catch (NotBoundException ex) {
            Logger.getLogger(Activitate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (MalformedURLException ex) {
            Logger.getLogger(Activitate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(Activitate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(Activitate.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

